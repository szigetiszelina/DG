        <li>Erős igék: ge + tőhangváltós szótő + en végződés. Pl.: helfen -&gt; geholfen, tőhangváltós szótő + en végződés nem elváló igekötős ige esetén. Pl.: besprechen -&gt; besproch<strong>en</strong>, igekötő + ge + en elváló igekötős ige esetén. Pl.: anbieten -&gt; angeboten</li>
        <li>Vegyes igék: ge + tőhangváltós szótő + t végződés. Pl.: kennen -&gt; gekannt, tőhangváltós szótő + t végződés nem elváló igekötős ige esetén. Pl.: erkennen -&gt; erkannt, igekötő + ge + tőhanváltós szótő + t elváló igekötős ige esetén. Pl.: nachdenken -&gt; nachgedacht</li>
        <li>A cselekvő személyét a “haben” vagy “sein” segédige megfelelően ragozott alakja mutatja meg</li>
    </ul>
